    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UrlDatabase db = new UrlDatabase();
        String connectionUrl = db.getUrl();
        try {
            Conn = DriverManager.getConnection(connectionUrl);
            St = Conn.createStatement();
            if ((!CosId.getText().isEmpty() && !CosName.getText().isEmpty() && !phone.getText().isEmpty())
                    && Long.parseLong(phone.getText()) > 0 && phone.getText().length() == 10) {

              
                String query = "insert into costomer(id, name , phone ) values ("
                        + "'" + CosId.getText() + "','" + CosName.getText() + "'," + Long.valueOf(phone.getText()) + ")";


                St.executeUpdate(query);
                new costomer().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "The Information not valid");
            }

        } // Handle any errors that may have occurred.
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Product is exsiting cannot add. Plese update product");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Phone must be number");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

