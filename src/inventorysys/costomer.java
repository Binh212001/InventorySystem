    private void fetchCustomerList() {
        UrlDatabase db = new UrlDatabase();
        String connectionUrl = db.getUrl();
        try {
            Conn = DriverManager.getConnection(connectionUrl);
            St = Conn.createStatement();
            Rs = St.executeQuery("select * from costomer");
            System.err.println(Rs);
            while (Rs.next()) {
                String Id = Rs.getString("id");
                String Name = Rs.getString("name");
                long Phone = Rs.getLong("phone");

                Object row[] = {Id, Name, Phone};
                DefaultTableModel model = (DefaultTableModel) CosTable.getModel();
                model.addRow(row);

                System.err.println("id" + Phone);

            }
        } catch (SQLException e) {
        }

    }

